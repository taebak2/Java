package sec01;

public class for문연습 {

	public static void main(String[] args) {

		// 87에서 34까지 출력, 반복횟수를 알 때 사용가능

		for (int i = 87; i > 33; i--) {
			System.out.println(i);
		}
		System.out.println("--------------------------------");
		// 32에서 59까지 짝수만 출력

		for (int j = 32; j < 59; j += 2) {
			System.out.println(j);
		}

		
	}

}

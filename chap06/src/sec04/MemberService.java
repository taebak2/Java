package sec04;
// 삽질..
public class MemberService {
	String login;
	String logout;

	public MemberService() {
	}

	public MemberService(String login, String logout) {
		this.login = login;
		this.logout = logout;
	}

	String login(String x, int y) {
		return login;
	}

	String logout(String x, int y) {
		return logout;
	}
}
